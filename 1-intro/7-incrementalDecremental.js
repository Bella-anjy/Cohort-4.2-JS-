/**
 * Incremental Operator ++
 */

let numberOfMangoesInBasket = 30;
numberOfMangoesInBasket++;
numberOfMangoesInBasket = numberOfMangoesInBasket + 10;

console.log("numberOfMangoesInBasket =", numberOfMangoesInBasket);

/**
 * Decremental Operator --
 */

let numberOfOrangesInStore = 50;
numberOfOrangesInStore--;
console.log("numberOfOrangesInStore =", numberOfOrangesInStore);
// numberOfOrangesInStore after sharing to 10 friends
numberOfOrangesInStore = numberOfOrangesInStore - 12;
console.log("numberOfOrangesInStore =", numberOfOrangesInStore);

