/**
 * Arithmetics
 * Addition operator +
 * Subtraction operator -
 * Divison operator /
 * Mulplication operator *
 * Modulus operator %
 */

// Addition operator
let firstValue = 22;
let secondValue = 20;

const additionResult = firstValue + secondValue;
console.log(additionResult); // 42

// Subtraction Operator

let x = 15;
let y = 12;
const z = x - y;
console.log("z =", z); // 3

// Divison

let s = 25;
let t = 5;
const u = s / t;
console.log("u = ", u); // 5

// Mulplication operator

let p = 12;
let q = 4;
let r = 13;
let mulplicationResult = p * q * r;
console.log("mulplicationResult =", mulplicationResult); // 624

// Modulo operator %

let a = 5;
let b = 2;

const moduloResult = a % b;
console.log("moduloResult =", moduloResult); // 1

/**
 * Adding strings together
 */

let initialValue = "34";
let finalValue = "64";

let finalResult = initialValue + finalValue;
console.log("finalResult", finalResult);
console.log("Data type of Final Result =>", typeof finalResult);
